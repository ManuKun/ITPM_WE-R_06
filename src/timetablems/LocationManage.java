    public void load(){
    
        int a;
        try{
            pst = conn.prepareStatement("select * from location");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            a= rd.getColumnCount();
            model = (DefaultTableModel)jTable2.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
            
                Vector v2 = new Vector();
                for(int i = 1; i<=a; i++){
                
                 v2.add(rs.getString("l_id"));
                 v2.add(rs.getString("building"));
                 v2.add(rs.getString("room"));
                 v2.add(rs.getString("r_subject"));
                 v2.add(rs.getString("capacity"));
                 v2.add(rs.getString("room_type"));
                
                }
                   model.addRow(v2);
            }
        }
        
        catch(Exception e){}
        finally {
              try{
                rs.close();
                pst.close();
            }
        catch(Exception e) {
            
        }}
    
    }
    
