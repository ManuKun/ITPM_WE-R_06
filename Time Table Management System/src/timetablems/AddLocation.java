    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
      String sql="INSERT INTO location(building,room,r_subject,capacity,room_type)VALUES(?,?,?,?,?)";
        
      try
      {
          pst = conn.prepareStatement(sql);
          pst.setString(1, building.getText());
          pst.setString(2, room.getText());
          pst.setString(3, rsubject.getText());
          pst.setString(4, capacity.getText());
          pst.setString(5, roomtype);
          
          pst.execute();
            JOptionPane.showMessageDialog(null, "System Update Completed");
            
            rs.close();
            pst.close();
      }
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null, e);
      }
      finally {
              try{
                rs.close();
                pst.close();
            }
        catch(Exception e) {
            
        }}
    }//GEN-LAST:event_saveActionPerformed

