    public void updateTable(){
    
        String sql="Select name from subject";
        
        try
        {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            Object[] columnData = new Object[1];
            
            while(rs.next()){
                columnData [0] = rs.getString("name");
                                
                model.addRow(columnData);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        finally {
              try{
                rs.close();
                pst.close();
            }
        catch(Exception e) {
            
        }}
        
    }
    
